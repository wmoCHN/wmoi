// 按语言拆分 data/content.js → data/lang/content.<lang>.js
// 用法: node tools/split-content-langs.js   (在仓库根目录运行)
//
// 改动文案的工作流: 仍然编辑源头 data/content.js,改完跑本脚本重新生成,
// 然后把 data/lang/ 的变化与源文件一起提交。页面加载的是 data/lang/ 下的文件。
//
// 裁剪规则(与 app.js 的读取语义一一对应):
// - t(value): value[currentLang] || value.en  → 保留当前语言键 + en 兜底键
// - localizedTitleLead/LeadLines/Lines: value[`${currentLang}Lead(/Lines)`] → 仅保留当前语言的后缀键
// - 其余键(结构、URL、数字、languages 菜单等)原样保留
const fs = require("fs");
const path = require("path");
const zlib = require("zlib");

const ROOT = path.join(__dirname, "..");
const LANGS = ["en", "zh", "ja", "ko", "th", "fr", "es"];
const PLAIN_RE = /^(en|zh|ja|ko|th|fr|es)$/;
const SUFFIX_RE = /^(en|zh|ja|ko|th|fr|es)(Lead|LeadLines|Lines)$/;

function loadContent(file) {
  const sandbox = { window: {} };
  // eslint-disable-next-line no-eval
  new Function("window", fs.readFileSync(file, "utf8"))(sandbox.window);
  return sandbox.window.SITE_CONTENT;
}

function assertNoFunctions(node, p) {
  if (typeof node === "function") throw new Error("内容树存在函数值,无法序列化: " + p);
  if (Array.isArray(node)) node.forEach((x, i) => assertNoFunctions(x, `${p}[${i}]`));
  else if (node && typeof node === "object") Object.entries(node).forEach(([k, v]) => assertNoFunctions(v, `${p}.${k}`));
}

function prune(node, L) {
  if (Array.isArray(node)) return node.map((x) => prune(x, L));
  if (node && typeof node === "object") {
    const out = {};
    for (const [k, v] of Object.entries(node)) {
      if (PLAIN_RE.test(k) && k !== L && k !== "en") continue;
      const m = k.match(SUFFIX_RE);
      if (m && m[1] !== L) continue;
      out[k] = prune(v, L);
    }
    return out;
  }
  return node;
}

// 模拟 app.js 全部读取语义,生成投影序列用于等价比对
function project(node, L, p, sink) {
  if (Array.isArray(node)) { node.forEach((x, i) => project(x, L, `${p}[${i}]`, sink)); return; }
  if (node && typeof node === "object") {
    sink.push(p + "#t=" + String(node[L] !== undefined ? node[L] : (node.en !== undefined ? node.en : "")));
    sink.push(p + "#lead=" + JSON.stringify(node[`${L}Lead`] ?? null));
    sink.push(p + "#leadLines=" + JSON.stringify(node[`${L}LeadLines`] ?? null));
    sink.push(p + "#lines=" + JSON.stringify(node[`${L}Lines`] ?? null));
    for (const [k, v] of Object.entries(node)) {
      if (PLAIN_RE.test(k) || SUFFIX_RE.test(k)) continue;
      project(v, L, `${p}.${k}`, sink);
    }
    return;
  }
  sink.push(p + "#v=" + JSON.stringify(node));
}

const SRC = loadContent(path.join(ROOT, "data/content.js"));
assertNoFunctions(SRC, "$");

const outDir = path.join(ROOT, "data/lang");
fs.mkdirSync(outDir, { recursive: true });

let failed = false;
for (const L of LANGS) {
  const pruned = prune(SRC, L);
  const file = path.join(outDir, `content.${L}.js`);
  fs.writeFileSync(
    file,
    "// AUTO-GENERATED by tools/split-content-langs.js — 不要手改,改 data/content.js 后重新生成\n" +
      "window.SITE_CONTENT = " + JSON.stringify(pruned) + ";\n"
  );
  // roundtrip 验证:回读写盘文件,与源树做全路径读语义比对
  const reloaded = loadContent(file);
  const a = [], b = [];
  project(SRC, L, "$", a);
  project(reloaded, L, "$", b);
  const ok = a.length === b.length && a.every((x, i) => x === b[i]);
  if (!ok) {
    failed = true;
    const i = a.findIndex((x, idx) => x !== b[idx]);
    console.error(`${L}: 不等价! 首个差异 @${i}\n  源: ${a[i]}\n  生成: ${b[i] || "(缺失)"}`);
  }
  const gz = zlib.gzipSync(fs.readFileSync(file)).length;
  console.log(`${L}: 等价=${ok}  ${(fs.statSync(file).size / 1024).toFixed(0)}KB (gzip ${(gz / 1024).toFixed(1)}KB)`);
}
if (failed) { process.exitCode = 1; } else { console.log("全部语言生成并验证通过"); }
